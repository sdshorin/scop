#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char cc;
	char *result;

	result = (char *)s;
	cc = (char)c;
	while (*result != '\0')
	{
		if (*result == cc)
			return (result);
		result++;
	}
	if (*result == cc)
		return (result);
	return (NULL);
}
